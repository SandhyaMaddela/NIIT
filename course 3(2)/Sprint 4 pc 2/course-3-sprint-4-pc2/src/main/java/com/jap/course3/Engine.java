package com.jap.course3;

public class Engine {
    String modelNo;
    String displacement;
    int maxPower;
    int maxRpm;
    int noOfCylinders;
    public Engine(String modelNo,String displacement,int maxPower,int maxRpm,int noOfCylinders){
        this.modelNo=modelNo;
        this.displacement=displacement;
        this.maxPower=maxPower;
        this.maxRpm=maxRpm;
        this.noOfCylinders=noOfCylinders;
    }

}
